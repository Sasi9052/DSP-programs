import numpy as np
from matplotlib import pyplot as plt
n=np.arange(1,250)
f=125
fs=5000
a=5*np.sin(2*np.pi*f/fs*n+np.pi/2)
b=10*np.sin(2*np.pi*f/fs*n+np.pi/2)
c=a+b
plt.subplot(3,1,1)
plt.stem(n,c)
plt.show()
f1=125
